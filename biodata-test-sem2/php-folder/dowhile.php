<?php
// Menggunakan struktur while
echo "<h2>Hasil dari perulangan while</h2>";
$baso = 0;
while ($baso > 1) {
    echo "Mmmm..Aku suka Baso! mak nyosssss....mantapp";
}

echo "<hr><br>";

// Menggunakan struktur do while
echo "<h2>Hasil dari perulangan do while</h2>";
$baso = 0;
do {
    echo "Mmmm..Aku suka Baso! mak nyosssss....mantapp";
} while ($baso > 1);
